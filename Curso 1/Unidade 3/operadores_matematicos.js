// Operadores Matemáticos
// Adição
let soma = 5 + 2;
console.log(soma); 

// Subtração
let subtracao = 5 - 2;
console.log(subtracao);

// Multiplicação
let multiplicacao = 5 * 2;
console.log(multiplicacao); 

// Divisão
let divisao = 5 / 2;
console.log(divisao); 

// Resto da divisão
let resto = 5 % 2;
console.log(resto); 

// Exponenciação
let exponenciacao = 5 ** 2;
console.log(exponenciacao); 

// Operadores de incremento e decremento
let numero = 5;

// Incremento e decremento que funcionam com todos os operadores matemáticos
numero = numero + 1;
numero += 3;
numero = numero - 2;
numero -= 2;

// Incremento e decremento que funcionam apenas com soma e subtração
numero++;
numero--;

console.log(numero);

// Operador de concatenação de strings
let nome = 'Daniela';
let complemento = ' e Costa';
let nomeCompleto = nome + complemento;

console.log(nomeCompleto); 